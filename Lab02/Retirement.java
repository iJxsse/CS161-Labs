// Java program that checks to see if the age of a person is greater than or equal to 66
// If it is, prints out that the person is retired; otherwise, print that they are not retired

public class Retirement {

  public static void main (String[] args) {

    int age = 65;

    if (age >= 66) {
      System.out.println("Person is retired");
    } else {
      System.out.println("Person is not retired");
    } 
  }
}
